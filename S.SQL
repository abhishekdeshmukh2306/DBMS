SET SERVEROUTPUT ON;
DECLARE 
NOD NUMBER;
RNO NUMBER;
NOB VARCHAR(20);
DOI DATE;
AMT NUMBER;
BEGIN 
RNO :=&RNO;
NOB := '&NOB';
SELECT DATEOFISSUE INTO DOI FROM BORROWER WHERE ROLL=RNO AND NAMEOFBOOK=NOB;
NOD :=SYSDATE -DOI;
IF (NOD >15 AND NOD<=30) THEN
AMT :=NOD * 5;
ELSIF (NOD >30) THEN
AMT := NOD *50;
ELSE 
DBMS_OUTPUT.PUT_LINE ('NO FINE');
END IF;
IF(NOD>15) THEN 
INSERT INTO FINE VALUES(RNO,SYSDATE, AMT);
END IF;
UPDATE BORROWER SET STATUS = 'R' WHERE ROLL=RNO AND NAMEOFBOOK=NOB;
EXCEPTION
WHEN NO_DATA_FOUND THEN
DBMS_OUTPUT.PUT_LINE ('NO RECORD');
WHEN OTHERS THEN
DBMS_OUTPUT.PUT_LINE ('ERROR');

END;
